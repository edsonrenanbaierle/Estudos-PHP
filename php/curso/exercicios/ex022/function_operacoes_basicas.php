<?php
    function somar($valor1, $valor2){
        return $valor1 + $valor2;
    }

    function subtrair($valor1, $valor2){
        return $valor1 - $valor2;
    }

    function divisao($valor1, $valor2){
        return $valor1 / $valor2;
    }

    function multiplicao($valor1, $valor2){
        return $valor1 * $valor2;
    }
?>